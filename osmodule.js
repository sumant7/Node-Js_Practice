const os = require('os');    //os is a built in module
console.log(os.freemem())   //shows free memory in computer
console.log(os.homedir())   //shows home directory 
console.log(os.hostname())   //shows host name
console.log(os.platform())   //shows win32  platform
console.log(os.release())   //shows release version os windows
console.log(os.totalmem())   // returns total memory
console.log(os.uptime())   //system uptime in seconds
console.log(os.type())   //shows os type...windows_NT



